package src.OOP_C.Encapsulation1;

public class Student {
        private String name;
        private int mark;
        public void setName(String n){
            name=n;
        }
        public String getName(){
            return name;
        }
        public void setMark(int m){
            mark=m;
        }
        public int getMark(){
            return mark;
        }
}
